#include "libft.h"

int	ft_isalpha(int c)
{
	int	tmp;

	tmp = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		tmp = 1 ;
	}
	return (tmp);
}
